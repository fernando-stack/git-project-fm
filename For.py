
# Loop for range from 0 to 4 counting two by two
for i in range(0, 5, +2):
    print(i)
# Loop for range from 4 to 0 regressive
for i in range(4, -1, -2):
    print(i)
# Loop with list
for i in ("Monday", "Tuesday", "Wednesday", "Thursday", "Friday"):
    print(i)
# Loop for times repeated
for i in (1, 2, 3):
    print(i, "repeat it")

